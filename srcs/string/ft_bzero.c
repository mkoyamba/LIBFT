#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		n;
	char	*t;

	if (!s)
		return ;
	t = (char *)s;
	n = 0;
	if (n == 0)
		return ;
	while (t[n])
	{
		t[n] = '0';
		n++;
	}
}
